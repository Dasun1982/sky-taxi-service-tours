function deepMerge(base, override) {
  if (Array.isArray(base) || Array.isArray(override)) {
    return override ?? base;
  }

  if (!base || typeof base !== "object") {
    return override ?? base;
  }

  const result = { ...base };
  Object.entries(override || {}).forEach(([key, value]) => {
    result[key] = deepMerge(base[key], value);
  });
  return result;
}

